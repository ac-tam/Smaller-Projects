def rotateMatrix90(list):
    rotated = []
    for i in range(len(list[0])):
        rotated.append([])
    for i in range(len(rotated)):
        for j in range(len(list)):
          rotated[i].append(0)
    for y in range(len(list)):
        for x in range(len(list[0])):
            rotated[x][y] = list[y][x]
    return rotated
